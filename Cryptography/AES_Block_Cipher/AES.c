/*Constructing LAT table*/
void lat_table(int LAT[][256]){
	int X[256][8], Y[256][8], inv_num, a_x_coeff[8], b_y_coeff[8], num, final_xor[256], x_xor, y_xor, zero_count, row,col,i,j,k,l;
	for( i=0;i<256;i++){
		num = i;
		for( j=0;j<8;j++){
			X[i][7-j] = num%2;
			num /= 2 ;
		}		
		row = 0; col = 0;
		for( j=0;j<4;j++)
			row = 2*row + X[i][j];
			
		for( j=4;j<8;j++)
			col = 2*col + X[i][j];
			
		inv_num = sbox[16*row+col];
		for( j=0;j<8;j++){
			Y[i][7-j] = inv_num%2;
			inv_num /= 2 ;
		}
	}	
	for( i=0;i<256;i++){
		num = i; row = i;
		for( k=0;k<8;k++){
			a_x_coeff[7-k] = num%2;
			num /= 2 ;
		}
		for( j=0;j<256;j++){
			num = j; col = j;
			for( k=0;k<8;k++){
				b_y_coeff[7-k] = num%2;
				num /= 2 ;
			}
			for( k=0;k<256;k++){
				x_xor = 0; y_xor = 0;
				for( l=0;l<8;l++){
					x_xor = x_xor ^ (a_x_coeff[l]*X[k][l]);
					y_xor = y_xor ^ (b_y_coeff[l]*Y[k][l]);
				}
				final_xor[k] = x_xor ^ y_xor; 
			}
			zero_count = 0;
			for( k=0;k<256;k++)
				if(final_xor[k] == 0)
					zero_count++;
					
			LAT[row][col] = zero_count;
		}
	}
	//printf("\n---------------------------------LAT------------------------------------\n");
	for( i=0;i<256;i++){
		for( j=0;j<256;j++){
			biasmatrix[i][j]= abs(LAT[i][j]-128)/256.0;
		}
	}

}
/*Constructing DT table*/
void dt_table(int DT[][256]){
	int i,j,k,n;
	for( i=0;i<256;i++){
		for ( j = 0; j < 256; j++){
			for( k=0;k<256;k++){
				if((i^j)== k){
					for( n=0;n<256;n++){
						if (n == (sbox[i]^sbox[j]))
							DT[k][sbox[i]^sbox[j]]++;
						}
				}
			}
		}
	}
	//printf("\n---------------------------------DT------------------------------------\n");
}

int sboxValue(int num){

	return sbox[num];
}

