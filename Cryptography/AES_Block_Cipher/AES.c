void keyExpansion(unsigned int key[]){

	int i, j;
	unsigned int temp[4];
	for(i=0;i<1;i++)
		for(j=0;j<16;j++)
				RoundKeys[i][j] = key[j];

	for(i=1;i<11;i++){
		temp[0] = RoundKeys[i-1][13];
		temp[1] = RoundKeys[i-1][14];
		temp[2] = RoundKeys[i-1][15];
		temp[3] = RoundKeys[i-1][12];
		

		temp[0] = sboxValue(temp[0]);
		temp[1] = sboxValue(temp[1]);
		temp[2] = sboxValue(temp[2]);
		temp[3] = sboxValue(temp[3]);
		

		temp[0] ^= Rcon[i];
		

		for(j=0;j<16;j += 4){
			if(j == 0){				
				RoundKeys[i][j] = RoundKeys[i-1][j] ^ temp[0];				
				RoundKeys[i][j+1] = RoundKeys[i-1][j+1] ^ temp[1];				
				RoundKeys[i][j+2] = RoundKeys[i-1][j+2] ^ temp[2];				
				RoundKeys[i][j+3] = RoundKeys[i-1][j+3] ^ temp[3];			
			}
			else{				
				RoundKeys[i][j] = RoundKeys[i-1][j] ^ RoundKeys[i][j-4];				
				RoundKeys[i][j+1] = RoundKeys[i-1][j+1] ^ RoundKeys[i][j-3];				
				RoundKeys[i][j+2] = RoundKeys[i-1][j+2] ^ RoundKeys[i][j-2];				
				RoundKeys[i][j+3] = RoundKeys[i-1][j+3] ^ RoundKeys[i][j-1];
			}
		}

	}
}

void byteSub(){
	int i,j;
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			state[i][j] = sboxValue(state[i][j]);
		
}

void shiftRows(){
	int temp;
	
	temp = state[1][0];
	state[1][0] = state[1][1];
	state[1][1] = state[1][2];
	state[1][2] = state[1][3];
	state[1][3] = temp;
	
	temp = state[2][0];
	state[2][0] = state[2][2];
	state[2][2] = temp;
	temp = state[2][1];
	state[2][1] = state[2][3];
	state[2][3] = temp;
		
	temp = state[3][3];
	state[3][3] = state[3][2];
	state[3][2] = state[3][1];
	state[3][1] = state[3][0];
	state[3][0] = temp;
}

void mixCol(){

	int i,j;

	unsigned int res[4][4];

		for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				res[i][j] = mul(diffusionMat[i][0],state[0][j])^mul(diffusionMat[i][1],state[1][j])^
							mul(diffusionMat[i][2],state[2][j])^mul(diffusionMat[i][3],state[3][j]);
		for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				state[i][j] = res[i][j];		
}

